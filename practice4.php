1. PHPフレームワークのなかでもLaravelの特徴を調べてみてください。
「多機能なMVCフレームワーク」
Laravelは、システム全体をMVCという、Model(データ処理)、View(画面表示)、Controller(全体の制御)の各機能に分けて整理し、これらのパーツごとに作成しながら開発を行う。
多機能(フルスタック)フレームワークであり、Webアプリケーションの様々な機能を自動生成することができる。
これにより実装部分における開発工数を減らすことができたり、経験の浅いエンジニアにも対応しやすいとして知られている。
「頻繁なバージョンアップ」
Laravelはバージョンアップが積極的に行われており、そのつど便利な新機能が追加されます。現在もシェアが広がり続けているため、プラグイン等の開発も盛んである。
バージョンアップの際には、バージョン更新サービスのLaravelShiftというツールを使えば、変更点に関する詳細なコメントや、Pull Requestを自動で生成してくれる。
「簡単に習得できる」
Laravelはくせがなくわかりやすいコードのため、初学者でも習得に時間がかからない。
多くの機能がありながらもその実装は容易であり、シンプルなコードで各機能を実現することができる。
学びはじめてすぐに使え、高品質なアプリが作れる。

